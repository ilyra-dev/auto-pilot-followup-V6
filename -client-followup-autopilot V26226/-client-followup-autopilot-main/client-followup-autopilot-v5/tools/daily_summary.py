def _get_today_followups():
    """
    Obtiene los seguimientos realizados hoy desde el log de drafts y el log de Notion.

    Returns:
        Lista de dicts con info de cada seguimiento del día
    """
    today_str = datetime.now(timezone.utc).strftime("%Y-%m-%d")
    followups = []

    # 1. Leer drafts_log.jsonl del día
    drafts_log = STYLE_DATA_DIR / "drafts_log.jsonl"
    if drafts_log.exists():
        try:
            with open(drafts_log, "r", encoding="utf-8") as f:
                for line in f:
                    line = line.strip()
                    if not line:
                        continue
                    try:
                        entry = json.loads(line)
                        ts = entry.get("timestamp", "")
                        if ts.startswith(today_str):
                            followups.append({
                                "proyecto": entry.get("project_name", "—"),
                                "cliente": entry.get("client_name", "—"),
                                "etapa": entry.get("stage", "?"),
                                "idioma": entry.get("language", "?"),
                                "asunto": entry.get("subject", "—"),
                                "destinatario": entry.get("to", "—"),
                                "modo": entry.get("status", SYSTEM_MODE),
                                "draft_id": entry.get("draft_id", ""),
                                "hora": ts[11:16] if len(ts) > 16 else "—",
                            })
                    except json.JSONDecodeError:
                        continue
        except Exception as e:
            logger.warning(f"Error leyendo drafts_log.jsonl: {e}")

    # 2. Complementar con Notion: buscar items con Last Follow-Up Date = hoy
    try:
        pages = notion_client.query_database()
        for page in pages:
            last_fu = notion_client.get_date_property(page, "Last Follow-Up Date")
            if last_fu == today_str:
                proj = notion_client.resolve_project_name(page)
                stage = notion_client.get_number_property(page, "Follow-Up Stage")
                # Evitar duplicados con drafts_log
                already = any(f["proyecto"] == proj and f["etapa"] == stage for f in followups)
                if not already:
                    followups.append({
                        "proyecto": proj or notion_client.get_text_property(page, "Nombre"),
                        "cliente": notion_client.resolve_client_name(page) or "—",
                        "etapa": stage,
                        "idioma": notion_client.get_select_property(page, "Client Language") or "—",
                        "asunto": "—",
                        "destinatario": "—",
                        "modo": SYSTEM_MODE,
                        "draft_id": "",
                        "hora": "—",
                    })
    except Exception as e:
        logger.warning(f"Error consultando Notion para resumen EOD: {e}")

    return followups


def _build_eod_blocks(followups):
    """
    Construye los bloques de Slack Block Kit para el resumen de fin de jornada.

    Args:
        followups: Lista de seguimientos del día

    Returns:
        Tuple de (blocks, fallback_text)
    """
    now = datetime.now(timezone.utc)
    date_display = now.strftime("%d/%m/%Y")
    total = len(followups)

    # Contadores por etapa
    by_stage = {}
    for fu in followups:
        s = fu["etapa"]
        by_stage[s] = by_stage.get(s, 0) + 1

    stage_names = {
        1: "📩 Recordatorio inicial",
        2: "📨 Segundo aviso",
        3: "🚨 Urgente",
        4: "🔴 Escalamiento",
    }

    # Header
    blocks = [
        {
            "type": "header",
            "text": {"type": "plain_text", "text": f"📊 Resumen de Seguimientos — {date_display}", "emoji": True}
        },
        {
            "type": "section",
            "text": {"type": "mrkdwn", "text": f"Se realizaron *{total} seguimiento{'s' if total != 1 else ''}* hoy | Modo: *{SYSTEM_MODE}*"}
        },
        {"type": "divider"},
    ]

    # Resumen por etapa
    if by_stage:
        stage_lines = []
        for stage_num in sorted(by_stage.keys()):
            name = stage_names.get(stage_num, f"Etapa {stage_num}")
            count = by_stage[stage_num]
            stage_lines.append(f"{name}: *{count}*")

        blocks.append({
            "type": "section",
            "text": {"type": "mrkdwn", "text": "*Desglose por etapa:*\n" + "\n".join(stage_lines)}
        })
        blocks.append({"type": "divider"})

    # Detalle de cada seguimiento (max 15 para no saturar Slack)
    shown = followups[:15]
    for fu in shown:
        stage_emoji = {1: "📩", 2: "📨", 3: "🚨", 4: "🔴"}.get(fu["etapa"], "📧")
        lang_flag = {"ES": "🇪🇸", "EN": "🇺🇸", "PT": "🇧🇷"}.get(fu["idioma"], "🌐")

        fields = [
            {"type": "mrkdwn", "text": f"*Proyecto:*\n{fu['proyecto']}"},
            {"type": "mrkdwn", "text": f"*Cliente:*\n{fu['cliente']}"},
            {"type": "mrkdwn", "text": f"*Etapa:*\n{stage_emoji} {fu['etapa']}"},
            {"type": "mrkdwn", "text": f"*Idioma:*\n{lang_flag} {fu['idioma']}"},
        ]

        blocks.append({"type": "section", "fields": fields})

        if fu.get("asunto") and fu["asunto"] != "—":
            blocks.append({
                "type": "context",
                "elements": [{"type": "mrkdwn", "text": f"📋 _{fu['asunto']}_"}]
            })

    if len(followups) > 15:
        blocks.append({
            "type": "context",
            "elements": [{"type": "mrkdwn", "text": f"_...y {len(followups) - 15} seguimiento(s) más._"}]
        })

    # Sin seguimientos
    if total == 0:
        blocks.append({
            "type": "section",
            "text": {"type": "mrkdwn", "text": "✅ No se realizaron seguimientos hoy. Todos los pendientes están al día o fuera de horario laboral."}
        })

    # Footer
    blocks.append({"type": "divider"})
    blocks.append({
        "type": "context",
        "elements": [
            {"type": "mrkdwn", "text": f"🤖 Generado por {COMPANY_NAME} Follow-Up Autopilot | {now.strftime('%H:%M UTC')}"}
        ]
    })

    fallback = f"Resumen de seguimientos ({date_display}): {total} seguimientos realizados hoy."
    return blocks, fallback


def send_eod_slack_summary():
    """
    Envía al canal de Slack un resumen de todos los seguimientos realizados durante el día.
    Diseñado para ejecutarse a las 5 PM hora Perú (22:00 UTC).

    Returns:
        Dict con resultado del envío o None
    """
    if not SLACK_REVIEW_CHANNEL:
        logger.warning("SLACK_REVIEW_CHANNEL no configurado. No se puede enviar resumen EOD.")
        return None

    logger.info("Generando resumen de fin de jornada...")

    followups = _get_today_followups()
    blocks, fallback_text = _build_eod_blocks(followups)

    result = slack_client.send_message(
        channel_id=SLACK_REVIEW_CHANNEL,
        text=fallback_text,
        blocks=blocks,
    )

    if result:
        logger.info(f"Resumen EOD enviado a Slack: {len(followups)} seguimientos")
    else:
        logger.error("Error enviando resumen EOD a Slack")

    return result

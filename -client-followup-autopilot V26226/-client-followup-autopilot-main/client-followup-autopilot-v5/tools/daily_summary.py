"""
Daily Summary para Client Follow-Up Autopilot.
Genera y envía reportes de actividad diaria al equipo CS vía email y Slack.
Incluye: items procesados, emails enviados/borradores, respuestas de clientes, métricas de aprendizaje.

Funciones:
  - send_daily_summary(): Resumen general por email + Slack
  - send_eod_slack_summary(): Resumen de seguimientos realizados al final de la jornada (Slack solamente)
"""

import json
import logging
from datetime import datetime, timezone, timedelta
from pathlib import Path

import gmail_client
import notion_client
import slack_client
import team_manager
from config import (
    GMAIL_DEFAULT_SENDER_EMAIL,
    SLACK_REVIEW_CHANNEL,
    SYSTEM_MODE,
    COMPANY_NAME,
    STYLE_DATA_DIR,
)
from style_store import load_metrics

logger = logging.getLogger(__name__)


def _count_items_by_status():
    """Count Notion items by status."""
    try:
        pages = notion_client.query_database()
        counts = {}
        for page in pages:
            status = notion_client.get_status_property(page, "Status") or "Unknown"
            counts[status] = counts.get(status, 0) + 1
        return counts, len(pages)
    except Exception as e:
        logger.error(f"Error counting items: {e}")
        return {}, 0


def generate_summary():
    """
    Generate a daily summary report.

    Returns:
        Dict with 'subject', 'body_html', 'body_text'
    """
    now = datetime.now(timezone.utc)
    date_str = now.strftime("%Y-%m-%d")

    # Get Notion status counts
    status_counts, total_items = _count_items_by_status()

    # Get learning metrics
    metrics = load_metrics()

    # Build summary
    subject = f"Follow-Up Autopilot — Daily Summary ({date_str})"

    # Status breakdown
    status_rows = ""
    for status in ["Sin empezar", "En curso", "Listo"]:
        count = status_counts.get(status, 0)
        if count > 0:
            status_rows += f"<tr><td>{status}</td><td><strong>{count}</strong></td></tr>"

    # Learning metrics
    approval_rate = metrics.get("approval_rate", 0)
    approval_pct = f"{approval_rate:.0%}" if approval_rate else "N/A"
    total_drafts = metrics.get("total_drafts", 0)
    sent_as_is = metrics.get("sent_as_is", 0)
    sent_edited = metrics.get("sent_edited", 0)
    discarded = metrics.get("discarded", 0)

    body_html = f"""
    <h2>📊 Follow-Up Autopilot — Daily Summary</h2>
    <p><strong>Date:</strong> {date_str} | <strong>Mode:</strong> {SYSTEM_MODE}</p>

    <h3>📋 Item Status Overview</h3>
    <table border="1" cellpadding="8" cellspacing="0" style="border-collapse:collapse;">
        <tr style="background:#f0f0f0;"><th>Status</th><th>Count</th></tr>
        {status_rows}
        <tr style="background:#f0f0f0;"><td><strong>Total</strong></td><td><strong>{total_items}</strong></td></tr>
    </table>

    <h3>🤖 Learning Metrics</h3>
    <table border="1" cellpadding="8" cellspacing="0" style="border-collapse:collapse;">
        <tr><td>Total drafts processed</td><td><strong>{total_drafts}</strong></td></tr>
        <tr><td>Sent as-is (no edits)</td><td><strong>{sent_as_is}</strong></td></tr>
        <tr><td>Sent with edits</td><td><strong>{sent_edited}</strong></td></tr>
        <tr><td>Discarded</td><td><strong>{discarded}</strong></td></tr>
        <tr><td>Approval rate</td><td><strong>{approval_pct}</strong></td></tr>
    </table>

    <h3>ℹ️ System Info</h3>
    <ul>
        <li><strong>Current Mode:</strong> {SYSTEM_MODE}</li>
        <li><strong>Mode Criteria:</strong> DRAFT → SEMI_AUTO at 80% approval | SEMI_AUTO → AUTO at 95% approval</li>
    </ul>

    <p style="color:#888; font-size:12px;">Generated by {COMPANY_NAME} Follow-Up Autopilot</p>
    """

    body_text = f"""Follow-Up Autopilot — Daily Summary ({date_str})
Mode: {SYSTEM_MODE}
Total items: {total_items}
Status: {json.dumps(status_counts)}
Drafts processed: {total_drafts} | Approval rate: {approval_pct}
"""

    return {
        "subject": subject,
        "body_html": body_html,
        "body_text": body_text,
    }


def send_daily_summary():
    """Generate and send the daily summary via email and Slack."""
    summary = generate_summary()

    # Send via email to all team members
    recipients = team_manager.get_daily_summary_recipients()
    for email_addr in recipients:
        email_result = gmail_client.send_email(
            to=email_addr,
            subject=summary["subject"],
            body_html=summary["body_html"],
            from_email=GMAIL_DEFAULT_SENDER_EMAIL,
        )
        if email_result:
            logger.info(f"Daily summary sent to {email_addr}")
        else:
            logger.error(f"Failed to send daily summary to {email_addr}")

    # Send via Slack
    if SLACK_REVIEW_CHANNEL:
        slack_client.send_message(
            channel_id=SLACK_REVIEW_CHANNEL,
            text=summary["body_text"],
            blocks=[
                {
                    "type": "header",
                    "text": {"type": "plain_text", "text": f"📊 Daily Summary — {datetime.now().strftime('%Y-%m-%d')}", "emoji": True}
                },
                {
                    "type": "section",
                    "text": {"type": "mrkdwn", "text": summary["body_text"]}
                },
            ]
        )
        logger.info("Daily summary posted to Slack")


if __name__ == "__main__":
    logging.basicConfig(level=logging.INFO)
    print("Generando resumen diario...\n")
    summary = generate_summary()
    print(summary["body_text"])


